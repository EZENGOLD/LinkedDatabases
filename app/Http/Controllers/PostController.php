    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(PostRequest $request)
    {
        try {
            $post = new Post(Universite::findOrFail($request->universite)->slug);
            $post->titre = $request->titre;
            $post->contenu = $request->contenu;
            $post->save();
            return redirect(route('universite.index'))->with([
                'success' => "Nouveau post ajouté dans le département de " . Universite::findOrFail($request->universite)->nom . " !"
            ]);
        } catch (Exception $e) {
            return redirect(route('universite.index'))->with([
                'error' => $e->getMessage()
            ]);
        }
    }

